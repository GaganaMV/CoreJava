package Array;

public class array {

	public static void main(String[] args) {
		int a[] = {1,2,3};
		System.out.println(a[1]);
		for(int i=0;i<a.length;i++) {
			System.out.println(a[i]);
			
		}
		for(int i=0;i<2;i++) {
			System.out.println(a[i]);
			
		}
		for(int i=0;i<=10;i++) {
			System.out.println(i);
		}
		for(int i=5;i>=0;i--) {
			System.out.println(i);
		}
}
}
